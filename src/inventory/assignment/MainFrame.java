    private void productSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productSearchBtnActionPerformed
        String searchContent = productSearchTf.getText();
        List<ProductDAO> products = productRepository.searchProduct(searchContent);
        renderProductTable(products);
    }//GEN-LAST:event_productSearchBtnActionPerformed

    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        int row = productTable.getSelectedRow();
        productIdTf.setText(model.getValueAt(row, 0).toString());
        productSkuTf.setText(model.getValueAt(row, 1).toString());
        productNameTf.setText(model.getValueAt(row, 2).toString());
        String selectedSupplierName = model.getValueAt(row, 3).toString();
        productQuantityTf.setText(model.getValueAt(row, 4).toString());
        productPriceTf.setText(model.getValueAt(row, 5).toString());

        DefaultComboBoxModel comboBoxModel = new DefaultComboBoxModel(supplierNames.toArray());
        productSupplierComboBox.setModel(comboBoxModel);
        productSupplierComboBox.setSelectedItem(selectedSupplierName);
    }//GEN-LAST:event_productTableMouseClicked

    private void productUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productUpdateBtnActionPerformed
        int id = Integer.parseInt(productIdTf.getText());
        String name = productNameTf.getText();
        String sku = productSkuTf.getText();
        double price = Double.parseDouble(productPriceTf.getText());
        int qty = Integer.parseInt(productQuantityTf.getText());
        int supplierId = supplierSelection.get(productSupplierComboBox.getSelectedItem().toString());
        ProductDAO p = new ProductDAO(id, sku, name, supplierId, price, qty);
        productRepository.updateProductByID(p);
        listAllProduct();
    }//GEN-LAST:event_productUpdateBtnActionPerformed

