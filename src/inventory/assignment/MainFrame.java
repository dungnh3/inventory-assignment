    private void renderProductTable(List<ProductDAO> products) {
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        model.setRowCount(0);
        for (ProductDAO product : products) {
            model.addRow(new Object[]{product.getId(), product.getSku(), product.getName(), product.getSupplierName(), product.getQuantity(), product.getPrice()});
        }
        productTableContainer.revalidate();
        productTableContainer.repaint();
    }

